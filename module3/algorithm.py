# Algoritmo para un palindromo

cadena_example = "ana"

# Invertir la cadena utilizando el operador de corte
reverse_cadena_example = cadena_example[::-1]

print(reverse_cadena_example)

# Comparar la cadena original con la invertida
es_palindromo = cadena_example == reverse_cadena_example

print(es_palindromo)
